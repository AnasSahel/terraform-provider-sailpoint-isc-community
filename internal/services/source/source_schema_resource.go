// Create implements resource.Resource.
func (r *sourceSchemaResource) Create(ctx context.Context, req resource.CreateRequest, resp *resource.CreateResponse) {
	var plan sourceSchemaModel
	tflog.Debug(ctx, "Getting plan for source schema resource")
	resp.Diagnostics.Append(req.Plan.Get(ctx, &plan)...)
	if resp.Diagnostics.HasError() {
		return
	}

	sourceID := plan.SourceID.ValueString()

	// Map resource model to API model
	tflog.Debug(ctx, "Mapping source schema resource model to API create request", map[string]any{
		"source_id": sourceID,
		"name":      plan.Name.ValueString(),
	})
	apiCreateRequest, diags := plan.ToAPI(ctx)
	resp.Diagnostics.Append(diags...)
	if resp.Diagnostics.HasError() {
		return
	}

	// Create the source schema via the API client
	tflog.Debug(ctx, "Creating source schema via SailPoint API", map[string]any{
		"source_id": sourceID,
		"name":      plan.Name.ValueString(),
	})
	createResponse, err := r.client.CreateSourceSchema(ctx, sourceID, &apiCreateRequest)
	if err != nil {
		resp.Diagnostics.AddError(
			"Error Creating SailPoint Source Schema",
			fmt.Sprintf("Could not create SailPoint Source Schema %q for source %q: %s",
				plan.Name.ValueString(), sourceID, err.Error()),
		)
		tflog.Error(ctx, "Failed to create SailPoint Source Schema", map[string]any{
			"source_id": sourceID,
			"name":      plan.Name.ValueString(),
			"error":     err.Error(),
		})
		return
	}

	if createResponse == nil {
		resp.Diagnostics.AddError(
			"Error Creating SailPoint Source Schema",
			"Received nil response from SailPoint API",
		)
		return
	}

	schemaID := createResponse.ID

	// Read back the authoritative state from the API to avoid inconsistencies
	// (the POST response may not include all fields accurately)
	tflog.Debug(ctx, "Reading back source schema after create for authoritative state", map[string]any{
		"source_id": sourceID,
		"schema_id": schemaID,
	})
	schemaResponse, err := r.client.GetSourceSchema(ctx, sourceID, schemaID)
	if err != nil {
		resp.Diagnostics.AddError(
			"Error Reading SailPoint Source Schema After Create",
			fmt.Sprintf("Source schema was created successfully but could not be read back. Schema ID %q for source %q: %s",
				schemaID, sourceID, err.Error()),
		)
		return
	}

	if schemaResponse == nil {
		resp.Diagnostics.AddError(
			"Error Reading SailPoint Source Schema After Create",
			"Received nil response from SailPoint API when reading back created schema",
		)
		return
	}

	// Map the authoritative GET response to the resource model
	var state sourceSchemaModel
	tflog.Debug(ctx, "Mapping SailPoint Source Schema API response to resource model", map[string]any{
		"source_id": sourceID,
		"schema_id": schemaID,
	})
	resp.Diagnostics.Append(state.FromAPI(ctx, *schemaResponse, sourceID)...)
	if resp.Diagnostics.HasError() {
		return
	}

	// Set the state
	tflog.Debug(ctx, "Setting state for source schema resource", map[string]any{
		"source_id": sourceID,
		"schema_id": state.ID.ValueString(),
	})
	resp.Diagnostics.Append(resp.State.Set(ctx, &state)...)
	if resp.Diagnostics.HasError() {
		return
	}
	tflog.Info(ctx, "Successfully created SailPoint Source Schema resource", map[string]any{
		"source_id":   sourceID,
		"schema_id":   state.ID.ValueString(),
		"schema_name": state.Name.ValueString(),
	})
}

// Read implements resource.Resource.
func (r *sourceSchemaResource) Read(ctx context.Context, req resource.ReadRequest, resp *resource.ReadResponse) {
	var state sourceSchemaModel
	tflog.Debug(ctx, "Getting state for source schema resource read")
	resp.Diagnostics.Append(req.State.Get(ctx, &state)...)
	if resp.Diagnostics.HasError() {
		return
	}

	sourceID := state.SourceID.ValueString()
	schemaID := state.ID.ValueString()

	// Read the source schema from SailPoint
	tflog.Debug(ctx, "Fetching source schema from SailPoint", map[string]any{
		"source_id": sourceID,
		"schema_id": schemaID,
	})
	schemaResponse, err := r.client.GetSourceSchema(ctx, sourceID, schemaID)
	if err != nil {
		// If resource was deleted outside of Terraform, remove it from state
		if errors.Is(err, client.ErrNotFound) {
			tflog.Info(ctx, "SailPoint Source Schema not found, removing from state", map[string]any{
				"source_id": sourceID,
				"schema_id": schemaID,
			})
			resp.State.RemoveResource(ctx)
			return
		}
		resp.Diagnostics.AddError(
			"Error Reading SailPoint Source Schema",
			fmt.Sprintf("Could not read SailPoint Source Schema %q for source %q: %s",
				schemaID, sourceID, err.Error()),
		)
		tflog.Error(ctx, "Failed to read SailPoint Source Schema", map[string]any{
			"source_id": sourceID,
			"schema_id": schemaID,
			"error":     err.Error(),
		})
		return
	}

	if schemaResponse == nil {
		resp.Diagnostics.AddError(
			"Error Reading SailPoint Source Schema",
			"Received nil response from SailPoint API",
		)
		return
	}

	// Map the response to the resource model
	tflog.Debug(ctx, "Mapping SailPoint Source Schema API response to resource model", map[string]any{
		"source_id": sourceID,
		"schema_id": schemaID,
	})
	resp.Diagnostics.Append(state.FromAPI(ctx, *schemaResponse, sourceID)...)
	if resp.Diagnostics.HasError() {
		return
	}

	// Set the state
	tflog.Debug(ctx, "Setting state for source schema resource", map[string]any{
		"source_id": sourceID,
		"schema_id": schemaID,
	})
	resp.Diagnostics.Append(resp.State.Set(ctx, &state)...)
	if resp.Diagnostics.HasError() {
		return
	}
	tflog.Info(ctx, "Successfully read SailPoint Source Schema resource", map[string]any{
		"source_id":   sourceID,
		"schema_id":   schemaID,
		"schema_name": state.Name.ValueString(),
	})
}

// Update implements resource.Resource.
func (r *sourceSchemaResource) Update(ctx context.Context, req resource.UpdateRequest, resp *resource.UpdateResponse) {
	var plan sourceSchemaModel
	tflog.Debug(ctx, "Getting plan for source schema resource update")
	resp.Diagnostics.Append(req.Plan.Get(ctx, &plan)...)
	if resp.Diagnostics.HasError() {
		return
	}

	// Get current state to retrieve the ID
	var state sourceSchemaModel
	resp.Diagnostics.Append(req.State.Get(ctx, &state)...)
	if resp.Diagnostics.HasError() {
		return
	}

	sourceID := state.SourceID.ValueString()
	schemaID := state.ID.ValueString()

	// Map resource model to API model (PUT requires full object with ID)
	tflog.Debug(ctx, "Mapping source schema resource model to API update request", map[string]any{
		"source_id": sourceID,
		"schema_id": schemaID,
	})

	// Set the ID on the plan model so ToAPIUpdate can include it
	plan.ID = state.ID
	apiUpdateRequest, diags := plan.ToAPIUpdate(ctx)
	resp.Diagnostics.Append(diags...)
	if resp.Diagnostics.HasError() {
		return
	}

	// Update the source schema via the API client (PUT)
	tflog.Debug(ctx, "Updating source schema via SailPoint API", map[string]any{
		"source_id": sourceID,
		"schema_id": schemaID,
	})
	_, err := r.client.UpdateSourceSchema(ctx, sourceID, schemaID, &apiUpdateRequest)
	if err != nil {
		resp.Diagnostics.AddError(
			"Error Updating SailPoint Source Schema",
			fmt.Sprintf("Could not update SailPoint Source Schema %q for source %q: %s",
				schemaID, sourceID, err.Error()),
		)
		tflog.Error(ctx, "Failed to update SailPoint Source Schema", map[string]any{
			"source_id": sourceID,
			"schema_id": schemaID,
			"error":     err.Error(),
		})
		return
	}

	// Read back the authoritative state from the API to avoid inconsistencies
	// (the PUT response may not include all fields accurately, e.g. boolean fields)
	tflog.Debug(ctx, "Reading back source schema after update for authoritative state", map[string]any{
		"source_id": sourceID,
		"schema_id": schemaID,
	})
	schemaResponse, err := r.client.GetSourceSchema(ctx, sourceID, schemaID)
	if err != nil {
		resp.Diagnostics.AddError(
			"Error Reading SailPoint Source Schema After Update",
			fmt.Sprintf("Source schema was updated successfully but could not be read back. Schema ID %q for source %q: %s",
				schemaID, sourceID, err.Error()),
		)
		return
	}

	if schemaResponse == nil {
		resp.Diagnostics.AddError(
			"Error Reading SailPoint Source Schema After Update",
			"Received nil response from SailPoint API when reading back updated schema",
		)
		return
	}

	// Map the authoritative GET response to the resource model
	var newState sourceSchemaModel
	tflog.Debug(ctx, "Mapping SailPoint Source Schema API response to resource model", map[string]any{
		"source_id": sourceID,
		"schema_id": schemaID,
	})
	resp.Diagnostics.Append(newState.FromAPI(ctx, *schemaResponse, sourceID)...)
	if resp.Diagnostics.HasError() {
		return
	}

	// Set the state
	tflog.Debug(ctx, "Setting state for source schema resource", map[string]any{
		"source_id": sourceID,
		"schema_id": newState.ID.ValueString(),
	})
	resp.Diagnostics.Append(resp.State.Set(ctx, &newState)...)
	if resp.Diagnostics.HasError() {
		return
	}
	tflog.Info(ctx, "Successfully updated SailPoint Source Schema resource", map[string]any{
		"source_id":   sourceID,
		"schema_id":   newState.ID.ValueString(),
		"schema_name": newState.Name.ValueString(),
	})
}

