// ImportState implements resource.ResourceWithImportState.
// Import format: source_id/schema_id.
func (r *sourceSchemaResource) ImportState(ctx context.Context, req resource.ImportStateRequest, resp *resource.ImportStateResponse) {
	tflog.Debug(ctx, "Importing source schema resource", map[string]any{
		"import_id": req.ID,
	})

	// Parse the import ID (format: source_id/schema_id)
	parts := strings.Split(req.ID, "/")
	if len(parts) != 2 || parts[0] == "" || parts[1] == "" {
		resp.Diagnostics.AddError(
			"Invalid Import ID",
			fmt.Sprintf("Expected import ID format: source_id/schema_id, got: %s", req.ID),
		)
		return
	}

	sourceID := parts[0]
	schemaID := parts[1]

	resp.Diagnostics.Append(resp.State.SetAttribute(ctx, path.Root("source_id"), sourceID)...)
	resp.Diagnostics.Append(resp.State.SetAttribute(ctx, path.Root("id"), schemaID)...)

	tflog.Info(ctx, "Successfully imported SailPoint Source Schema resource", map[string]any{
		"source_id": sourceID,
		"schema_id": schemaID,
	})
}
